    private void jBOblicz1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBOblicz1ActionPerformed
        //Prostopadłościan
        int a = Integer.parseInt(jTFBokAP.getText());
        int b = Integer.parseInt(jTFBokBP.getText());
        int c = Integer.parseInt(jTFBokCP.getText());
        //oblicznie pola prostopadłościanu
        int pole = (2*a*b)+(2*a*c)+(2*b*c);
        jTFPoleP.setText(pole+"");
        //oblicznie objętości
        int obj = a*b*c;
        jTFObjP.setText(obj+"");
        //oblicznie przekątnej
        int temp = (int) (Math.pow(a, 2)+Math.pow(b, 2)+Math.pow(c, 2));
        int przek = (int) Math.sqrt(temp);
        jTextFieldPrzekP.setText(przek+"");

    }//GEN-LAST:event_jBOblicz1ActionPerformed

