    private void jBOblicz3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBOblicz3ActionPerformed
        // Ostrosłup Prawidłowy czworokątny
        int a = Integer.parseInt(jTFBokAO.getText());
        int h = Integer.parseInt(jTFBokHO.getText());
        //oblicznie Pola
        int poleP = a*4;
        int poleB = ((a*h/2)*4);
        int poleCal = poleP + poleB;
        jTFPoleO.setText(poleCal+"");
        //obliczanie Objętości
        int obj = (int) (0.33*(poleP*h));
        jTFObjO.setText(obj+"");
    }//GEN-LAST:event_jBOblicz3ActionPerformed

