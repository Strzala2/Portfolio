    private void jBObliczActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBObliczActionPerformed
        //Czworokąt Foremny
        int a = Integer.parseInt(jTFBokA.getText());
        int h = Integer.parseInt(jTFBokH.getText());
        //oblicznie pola
        int pole = (int) ((int) Math.pow(a, 2)*Math.sqrt(3));
        jTFPole.setText(pole+"");
        //oblicznie objętości
        int obj = (int) (Math.pow(a, 3)*Math.sqrt(2)/12);
        jTFObj.setText(obj+"");
    }//GEN-LAST:event_jBObliczActionPerformed

